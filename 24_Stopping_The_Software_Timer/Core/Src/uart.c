int LPUART1_write(int ch) {
	while(!(LPUART1->ISR & 0x0080)){}
	LPUART1->RDR = (ch & 0xFF);
	return ch;
}

int __io_putchar(int ch) {
	LPUART1_write(ch);
	return ch;
}
